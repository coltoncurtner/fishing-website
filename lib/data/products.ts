import type { Product, Variant } from "@/lib/types";

let imgCounter = 0;
function imgs(name: string, n = 4) {
  return Array.from({ length: n }, (_, i) => ({
    seed: `${name}-${i}-${imgCounter++}`,
    alt: `${name} — view ${i + 1}`,
  }));
}

function variants(
  label: string,
  values: Array<[string, number?, number?]>,
): Variant[] {
  return values.map(([value, priceDelta, stock], i) => ({
    id: `${label}-${i}`.toLowerCase().replace(/\s+/g, "-"),
    label,
    value,
    priceDelta: priceDelta ?? 0,
    stock: stock ?? 24,
  }));
}

